    <p>Allerede i 2004 begyndte man at opnå lovende resultater indenfor juridiske forudsigelser af retssager baseret på AI og machine Learning. Washington University udviklede deres juridiske AI, der efterfølgende blev testet af på 628 afgørelser fra den amerikanske højesteret og sammenlignede det med resultatet fra en gruppe af juridiske eksperter. <strong>vandt med 75% korrekte forudsigelser</strong> – sammenlignede med eksperterne der kun nåede op på 59%.</p>
